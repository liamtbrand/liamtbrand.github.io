03/12/2013<br />
MagicWorlds version <b>03.12.13a</b> is done.
<br />
New Features:
<br />
<b>1. Added Object Layer</b> - This allows for objects to be placed on the object layer.
<br />
<b>2. Added Objects</b> - These will appear on the editing panel.
<br />
<b>3. Server Version Check</b> - This will make sure the client and server are compatible before connection is fully made.
<br />
<b>4. New Map Format</b> - I updated the map format to include both the terrain and object layers. Old maps will be converted to new ones when loaded, but they will only save in the newest format.
<br />
Updates:
<br />
<b>1.</b> Fixed crash when clicking on edges around map. (This was caused by an access violation in the map array..)
<br />
<b>2.</b> Fixed bugs caused by threads not synchronizing properly. (Let me know if they persist)
<br />
<b>3.</b> Fixed bug that client places tile right as it joins the server.
