<p>
07/04/2015<br />
MagicWorlds version <b>07.04.15a</b> is done. Almost everything has been rebuilt from the ground up. This latest version supports maps edited with Tiled.
<br />
New Features:
<br />
<b>+ Tiled map support</b> - This allows for maps to be build much quicker using tiled which exports to xml and csv formatting.
<br />
<b>+ Players</b> - Other players appear in game.
<br />
<b>+ Server Version Check</b> - This will make sure the client and server are compatible before connection is fully made.
<br />
<b>+ Portals</b> - Allow for players to get between maps.
<br />
<b>+ Client sided maps.</b>
<br />
<b>+ Server sided user data</b> - Users now have their location and inventory saved upon disconnection.
<br />
<b>- Removed old mapping system.</b>
<br />
Fixes:
<br />
<b>*</b> Other players now update their orientations correctly.
<br />
<b>*</b> Built in pre-rendering of maps. This fixes previous frame rate issues.
<br />
